package convertidor.modelo;

public class Moneda {
	private String nombre;
	private String pais;
	
	public String getNombre() {
		return nombre;
	}
	public String getPais() {
		return pais;
	}
	
	public void setNombre(String n) {
		this.nombre = n;
	}
	public void setPais(String p) {
		this.pais = p;
	}
	
}
